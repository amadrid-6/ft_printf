#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char			*p;
	unsigned	int	i;

	p = s;
	i = 0;
	while (i < n)
	{
		p[i] = c;
		i++;
	}
	return (p);
}
